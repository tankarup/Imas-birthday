function get_prompt(date, label){
  let text = label + date + 'は';
  let speech = text;
  let count = 0;
  for (let name in idols){
    if (idols[name]['誕生日'] == date){
      
      const pro = (idols[name]['事務所'])? '(' + idols[name]['事務所'] + ')' : '';
      const pro_kana = (idols[name]['じむしょ'])? ' ' + idols[name]['じむしょ'] + ' ' : '';
      const kana = (idols[name]['なまえ'])? idols[name]['なまえ'] : idols[name]['名前'];
      text += '、' + pro + idols[name]['名前'] + 'さん';
      speech += '、' + pro_kana + kana + ' さん';

      count += 1;
    }
  }
  text += 'の誕生日です。';
  speech += 'の誕生日です。';
  if (count == 0){
    text = label + date + 'が誕生日のアイドルはいません。';
    speech = text;
  }
  return [text, speech];
}
